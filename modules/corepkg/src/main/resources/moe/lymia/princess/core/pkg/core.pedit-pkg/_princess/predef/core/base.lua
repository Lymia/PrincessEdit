local _princess = ...
local core = _princess.core

local LogLevel, log_fn, tostring, where, concat, gameId =
    core.LogLevel, core.log, tostring, core.where, table.concat, core.gameId

log = {}

for name, logLevel in pairs(LogLevel) do
    name = name:lower()
    log[name] = function(...)
        local args = {... }
        local loc = where(1)
        if loc then loc = "script "..loc end
        return log_fn(logLevel, where(1), function()
            local len = #args
            for i=1,len do
                args[i] = tostring(args[i])
            end
            return concat(args, "\t", 1, len)
        end)
    end
end

print = log.info
_G.gameId = gameId
